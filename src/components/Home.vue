<template>
    <div>
        <i-circle :percent="percent" :stroke-color="color">
            <Icon v-if="percent == 100" type="ios-checkmark-empty" size="60" style="color:#5cb85c"></Icon>
            <span v-else style="font-size:24px">{{ percent }}%</span>
        </i-circle>
        <Button-group size="large">
            <Button icon="ios-plus-empty" @click="add"></Button>
            <Button icon="ios-minus-empty" @click="minus"></Button>
        </Button-group>
        <Row>
            <Col span="8">1</Col>
            <Col span="8">2</Col>
            <Col span="8">3</Col>
        </Row>
    </div>
</template>

<script>
export default {
    data() {
        return {
            percent: 0
        }
    },
    computed: {
        color() {
            let color = '#2db7f5';
            if (this.percent == 100) {
                color = '#5cb85c';
            }
            return color;
        }
    },
    methods: {
        add() {
            if (this.percent >= 100) {
                return false;
            }
            this.percent += 10;
        },
        minus() {
            if (this.percent <= 0) {
                return false;
            }
            this.percent -= 10;
        }
    }
}
</script>

